
class Credentials:
	def __init__(self, host, user, passwd):
		self.host = host
		self.user = user
		self.passwd = passwd

	def getHost():
		return self.host
	def getUser():
		return self.user
	def getPasswd():
		return self.passwd

	def setHost():
		self.host = host
	def setUser():
		self.user = user
	def setPasswd():
		self.passwd = passwd